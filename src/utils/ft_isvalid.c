#include "../../pushswap.h"

int	ft_isvalid(char c, int ix)
{
	if (ft_isdigit(c))
		return (1);
	if (c == ' ')
		return (1);
	if (ix == 0 && (c == '-' || c == '+'))
		return (1);
	return (0);
}
